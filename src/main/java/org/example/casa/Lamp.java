package org.example.casa;

public class Lamp {
    public int bulbsNumber;
    public String color;
    public boolean myLamp;
    public int height;

    public int getBulbsNumber() {
        return bulbsNumber;
    }

    public void setBulbsNumber(int bulbsNumber) {
        this.bulbsNumber = bulbsNumber;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public boolean isMyLamp() {
        return myLamp;
    }

    public void setMyLamp(boolean myLamp) {
        this.myLamp = myLamp;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
