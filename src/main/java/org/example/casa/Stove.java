package org.example.casa;

public class Stove {
    public boolean myStove;
    public String color;
    public String type;
    public int eyesNumber;
    public int price;
    public String brand;

    public boolean isMyStove() {
        return myStove;
    }

    public void setMyStove(boolean myStove) {
        this.myStove = myStove;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getEyesNumber() {
        return eyesNumber;
    }

    public void setEyesNumber(int eyesNumber) {
        this.eyesNumber = eyesNumber;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
}